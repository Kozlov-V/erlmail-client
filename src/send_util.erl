encode_mail(From, To, Cc, Bcc, Subject, Body, []) when is_list(Body), is_integer(hd(Body)) ->
    Headers = gen_headers(From, To, Cc, Bcc, Subject),
    ?D({headers, Headers}),
    Email = body_to_mime(Body, Headers),
    mimemail:encode(Email);

encode_mail(From, To, Cc, Bcc, Subject, {html, _Content} = Body , []) ->
    Headers = gen_headers(From, To, Cc, Bcc, Subject),
    Email = body_to_mime(Body, Headers),
    mimemail:encode(Email);

encode_mail(From, To, Cc, Bcc, Subject, Body, Attatchments) ->
    Headers = gen_headers(From, To, Cc, Bcc, Subject),
    BodyPart = body_to_mime(Body, []),
    AttachPart = attach_to_mime(Attatchments, []),
    FinalBody = case is_tuple(BodyPart) of
                    true ->
                        [BodyPart|AttachPart];
                    _ ->
                        BodyPart ++ AttachPart
                end,
    MimeMail = {<<"multipart">>, <<"mixed">>, Headers,
                [{<<"content-type-params">>,
                  [{<<"charset">>,<<"UTF-8">>}],
                  {<<"disposition">>,<<"inline">>}}],
                FinalBody},
    mimemail:encode(MimeMail).



%%
%% Local Functions
%%
gen_headers(From, To, Cc, Bcc, Subject) ->
    ToList = [{<<"To">>, list_to_binary(Address)}|| Address <-To],
    ?D({To, ToList}),
    CcList = [{<<"Cc">>, list_to_binary(Address)}|| Address <-Cc],
    ?D({Cc, CcList}),
    BccList = [{<<"Bcc">>, list_to_binary(Address)}|| Address <-Bcc],
    ?D({Bcc, BccList}),
    [{<<"From">>, list_to_binary(From)},
     {<<"Subject">>, list_to_binary(Subject)},
     {<<"MIME-Version">>, <<"1.0">>}] ++ ToList ++ CcList ++ BccList.

%% Case for text/html
body_to_mime({html, Content}, Headers) ->
    {<<"text">>, <<"html">>, Headers,
     [{<<"content-type-params">>,
       [{<<"charset">>,<<"UTF-8">>}],
       {<<"disposition">>,<<"inline">>}}],
     list_to_binary(Content)};
%% Case for text/plain
body_to_mime(Content, Headers) when is_list(Content), is_integer(hd(Content))->
    {<<"text">>, <<"plain">>, Headers,
     [{<<"content-type-params">>,
       [{<<"charset">>,<<"UTF-8">>}],
       {<<"disposition">>,<<"inline">>}}],
     list_to_binary(Content)};
%% Case for mixed text contains both plain and html
body_to_mime({mixed, List}, Headers) ->
    [body_to_mime(Item, Headers)||Item<-List].


    

%% attachment
attach_to_mime([], Res) ->
    lists:reverse(Res);
attach_to_mime([{Name, Content}|T], Res) ->
    attach_to_mime(T, [attachment(Name, Content)|Res]).

attachment(FileName, Content) when is_list(FileName) ->
    attachment(list_to_binary(FileName), Content);
attachment(FileName, Content) when is_list(Content) ->
    attachment(FileName, list_to_binary(Content));
attachment(FileName, Content) when is_binary(FileName), is_binary(Content) ->
    {MimeType, Type, SubType, Render} = mime_from_ext(FileName),
    Headers = [{<<"Content-Disposition">>,
                <<Render/binary, ";filename=", FileName/binary>>},
               {<<"Content-Type">>, MimeType}],
    Properties = [{<<"content-type-params">>,
                   [{<<"name">>,FileName}]},
                  {<<"disposition">>,Render},
                  {<<"disposition-params">>,
                   [{<<"filename">>,FileName}]}],
    {Type, SubType, Headers, Properties, Content}.

mime_from_ext(FileName) ->
    ?D(FileName),
    Ext = case filename:extension(binary_to_list(FileName)) of
              "." ++ Rest -> Rest;
              Val -> Val
          end,
    {_, MimeType} = t(Ext),
    [Type, SubType|_] = string:tokens(MimeType, "/"),
    RenderType = case Type of
                     "image" -> <<"inline">>;
                     _ -> <<"attachment">>
                 end,
    ?D({MimeType, Type, SubType, RenderType}),
    {list_to_binary(MimeType),
     list_to_binary(Type), 
     list_to_binary(SubType), 
     RenderType}.



t(Ext) ->
    t(global, Ext).


